    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try{
          ResultSet rs = utl.st.executeQuery("select vname,vaddr,vgender,vage,issue,curdate from new_record");
          DefaultTableModel dft = (DefaultTableModel) tbl.getModel();
          dft.setRowCount(0);
          while(rs.next()){
              Object o[] = { rs.getString("vname"),rs.getString("vaddr"),rs.getString("vgender"),rs.getString("vage"),rs.getString("issue"),rs.getString("curdate")};
              dft.addRow(o);
              
          }
          tbl.setModel(dft);
          tbl.setShowGrid(true);
          tbl.setEnabled(false);
          tbl.setForeground(Color.white);
          tbl.setBackground(Color.blue);
          tbl.setRowHeight(24);
      
          tbl.setFont(new java.awt.Font("Tahoma", 1, 13));
          tbl.setCellSelectionEnabled(false); 
          
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

