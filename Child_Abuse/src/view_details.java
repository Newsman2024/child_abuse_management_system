    private void txtidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidKeyReleased
        try{
            ResultSet rs = utl.st.executeQuery("select nname,addr,issue,ge,phn,sit,curdate,age,vname,vaddr,vgender,vage from new_record where ID = '"+txtid.getText()+"'");

            if(rs.next()){
                txtnname.setText(rs.getString(1));
                txtaddr.setText(rs.getString(2));
                txtissue.setText(rs.getString(3));
                txtgender.setText(rs.getString(4));
                txtphone.setText(rs.getString(5));
                txtsitua.setText(rs.getString(6));
                txtdate.setText(rs.getString(7));
                txtage.setText(rs.getString(8));

                txtname.setText(rs.getString(9));
                txtaddr.setText(rs.getString(10));
                txtgen.setText(rs.getString(11));
                txtag.setText(rs.getString(12));
                lblf.setText("One Record Found");
                lble.setBackground(Color.green);
                txtnname.setEditable(false);
                txtaddr.setEditable(false);
                txtissue.setEditable(false);
                txtgender.setEditable(false);
                txtphone.setEditable(false);
                txtsitua.setEditable(false);
                txtdate.setEditable(false);
                txtage.setEditable(false);
                txtname.setEditable(false);
                txtaddr.setEditable(false);
                txtag.setEditable(false);

            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_txtidKeyReleased

